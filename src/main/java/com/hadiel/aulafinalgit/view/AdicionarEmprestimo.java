    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (emprestimo == null) {
            if (!emprestimoDAO.verificarPendencias(usuarios.get(jComboBox2.getSelectedIndex()).getId())) {
                emprestimo = new Emprestimo();
                emprestimo.setDataDevolucao(jFormattedTextField2.getText().trim());
                emprestimo.setDataLocacao(jFormattedTextField1.getText().trim());
                emprestimo.setLivroCodigo(livros.get(jComboBox1.getSelectedIndex()).getId());
                emprestimo.setUsuarioCodigo(usuarios.get(jComboBox2.getSelectedIndex()).getId());

                emprestimo.setStatus("Emprestado");
                if (jRadioButton2.isSelected()) {
                    emprestimo.setStatus("Disponível");
                }

                if (emprestimoDAO.realizarEmprestimo(emprestimo)) {
                    Livro livro = livros.get(jComboBox1.getSelectedIndex());
                    livro.setDisponivel(!jRadioButton1.isSelected());
                    livroDAO.editarLivro(livro);
                    dispose();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Esse usuário possui devoluções pendentes.", "Falha no Empréstimo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            emprestimo.setDataDevolucao(jFormattedTextField2.getText().trim());
            emprestimo.setDataLocacao(jFormattedTextField1.getText().trim());
            emprestimo.setLivroCodigo(livros.get(jComboBox1.getSelectedIndex()).getId());
            emprestimo.setUsuarioCodigo(usuarios.get(jComboBox2.getSelectedIndex()).getId());
            emprestimo.setStatus("Emprestado");
            if (jRadioButton2.isSelected()) {
                emprestimo.setStatus("Disponível");
                if (emprestimoDAO.atualizarEmprestimo(emprestimo)) {
                    Livro livro = livros.get(jComboBox1.getSelectedIndex());
                    livro.setDisponivel(true);
                    livroDAO.editarLivro(livro);
                    dispose();
                }
            } else {
                if (emprestimoDAO.atualizarEmprestimo(emprestimo)) {
                    dispose();
                }
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


