    private void preencherComboBoxUsuarios() {
        jComboBox2.removeAllItems();
        usuarios = usuarioDAO.listarUsuarios(" ORDER BY nome");
        int cont = 0;
        for (Usuario usuario : usuarios) {
            if (emprestimo != null) {
                if (emprestimo.getUsuarioCodigo() == usuario.getId()) {
                    indexUsuario = cont;
                }
            }
            jComboBox2.addItem(usuario.getNome());
            cont++;
        }
    }

    private void preencherComboBoxLivros() {
        jComboBox1.removeAllItems();
        String disponivel = "true";
        if (emprestimo != null) {
            disponivel = "false";
        }
        livros = livroDAO.listarLivros(" WHERE disponivel = '" + disponivel + "' ORDER BY titulo");
        int cont = 0;
        for (Livro livro : livros) {
            if (emprestimo != null) {
                if (emprestimo.getLivroCodigo() == livro.getId()) {
                    indexUsuario = cont;
                }
            }
            jComboBox1.addItem(livro.getTitulo());
            cont++;
        }
    }

    public static String getDataHj() {
        // Obter a data atual
        LocalDate hoje = LocalDate.now();

        // Formatando a data atual para dd/MM/aaaa
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        return hoje.format(formato);
    }

    public static String calculaSeteDias(String data) {
        // Converter a string de data para LocalDate
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataFormatada = LocalDate.parse(data, formato);

        // Adicionar 7 dias à data fornecida
        LocalDate dataDaquiA7Dias = dataFormatada.plusDays(7);

        // Formatando a nova data para dd/MM/aaaa
        return dataDaquiA7Dias.format(formato);
    }

